package the.maze.objects;

/**
 *
 * @author Marc
 */
public class GameObject {
    
    int x;
    int y;
    
    
    /**
     * CONSTRUCTOR
     */
    public GameObject() {
    }

    
    /**
     * GET X
     */
    public int getX() {
        return x;
    }

    
    /**
     * SET X
     */
    public void setX(int x) {
        this.x = x;
    }

    
    /**
     * GET Y
     */
    public int getY() {
        return y;
    }

    
    /**
     * SET Y
     */
    public void setY(int y) {
        this.y = y;
    }
    
}